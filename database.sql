BEFORE INSERT ON LanKham
FOR EACH ROW
BEGIN
    DECLARE seq INT;
    DECLARE ma_bs VARCHAR(20);

    SELECT MaNV INTO ma_bs FROM NhanVien WHERE ID_NHANVIEN = NEW.ID_BS;

    SELECT COUNT(*)+1 INTO seq
    FROM LanKham
    WHERE DATE(ThoiGianKham) = DATE(NEW.ThoiGianKham)
      AND ID_BS = NEW.ID_BS
      AND KhoaKham = NEW.KhoaKham;

    SET NEW.MaKham = CONCAT(
        LEFT(NEW.KhoaKham, 3), -- Lấy 3 ký tự đầu của khoa
        '-', 
        ma_bs, 
        '-', 
        DATE_FORMAT(NEW.ThoiGianKham, '%Y%m%d'), 
        '-', 
        LPAD(seq,3,'0')
    );
